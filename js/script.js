document.getElementById('btn1').addEventListener("click", () => {
    const para = document.getElementById("para1")
    let newPara = para.innerText
    const numberPara = parseInt(newPara) + 1
    para.innerText = numberPara
    // price total 
    // getting price 1
    const price = document.getElementById("price1")
    // taking price into innertext
    let nwePrice = price.innerText
    // converting new price into number
    const numberPrice = parseInt(nwePrice)
    // getting cost 
    const cost = document.getElementById("cost")
    // taking cost int inner Text
    let newCost = cost.innerText
    // converting new cost into number
    const numberCost = parseInt(newCost)
    // summission of cost and price
    cost.innerText = numberPrice + numberCost
    // getting total price
    const total = document.getElementById("total")
    // taking total price into inner text 
    let newTotal = total.innerText
    // converting new Total into number
    const numberTotal = parseInt(newTotal)
    // summission of numberTotal and numberCost
    total.innerText = numberTotal + numberPrice

})


